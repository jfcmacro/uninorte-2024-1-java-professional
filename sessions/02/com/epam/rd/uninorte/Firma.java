package com.epam.rd.uninorte;

public class Firma {

    // Firma (Signature) nombre parametros
    public static void funcion() {
        System.out.println("funcion");
    }

    public static void funcion(int i) {
        System.out.println(i);
    }

    public static int funcion(int i) {
        return i;
    }
}
          
